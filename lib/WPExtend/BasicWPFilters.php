/**
 * Autoloder for WordPress Filter Hooks.
 * Instead of calling for each method an add_filter method, all class methods can be hooked automatically.
 * 
 * @author Dave A. Holyfield
 * @since 2.0.7
 * @version 2.0.8
 */
final class BasicWPFilters {

    /**
     */
    private function __construct () {
    }

    public static function GetFiltersList () {
        return self::$_filters;
    }

    public static function Debug () {
        $filters = array_unique( array_keys( self::$_filters ) );
        sort( $filters );
        foreach ( $filters as $action ) {
            $items = preg_split( "/_|-/uiU", $action );
            foreach ( $items as $key => $value ) {
                if ( $value == 'wp' ) {
                    $items[$key] = strtoupper( $value );
                } else {
                    $items[$key] = ucfirst( $value );
                }
            }
            $name = implode( '', $items );
            $name = (substr( $action, 0, 1 ) == '_') ? '_' . $name : $name;
            echo '\'' . $action . '\' => \'OnFilter' . $name . '\',' . PHP_EOL;
        }
    }

    /**
     *
     * @param array $filters            
     * @return array
     * @since 2.0.7
     */
    public static function GetFilterDefinitions ( $filters = null ) {
        static $default;
        if ( is_null( $filters ) ) {
            if ( ! is_array( $default ) ) {
                $default = array ();
                foreach ( self::$_filters as $key => $filt ) {
                    $default[$key] = array ( 
                        'method' => $filt,
                        'priority' => 10,
                        'args' => (array_key_exists( $key, self::$_filters_args )) ? self::$_filters_args[$key] : 1
                    );
                }
            }
            return $default;
        } elseif ( is_array( $filters ) && count( $filters ) > 0 ) {
            foreach ( $filters as $key => $filt ) {
                $filters[$key] = array ( 
                    'method' => $filt,
                    'priority' => 10,
                    'args' => (array_key_exists( $key, self::$_filters_args )) ? self::$_filters_args[$key] : 1
                );
            }
            return $filters;
        }
        return array ();
    }

    /**
     *
     * @param string $class            
     * @since 2.0.7
     */
    public static function GetClassFilterMethods ( $class ) {
        $filters = self::GetFiltersList();
        $statics = BasicWPActions::GetStaticMethods( $class );
        $methods = array_intersect( $filters, $statics );
        return self::GetFilterDefinitions( $methods );
    }

    public static function HookAllClassFilterMethods ( $class, $priorites = null ) {
        // If class does not exists, try to load this class
        if ( ! class_exists( $class ) ) {
            AutoLoad::LoadClass( $class );
        }
        // Hook class methods
        if ( class_exists( $class ) ) {
            $methods = self::GetClassFilterMethods( $class );
            if ( is_array( $priorites ) && count( $priorites ) > 0 ) {
                foreach ( $priorites as $id => $priority ) {
                    if ( array_key_exists( $id, $methods ) ) {
                        $methods[$id]['priority'] = $priority;
                    }
                }
            }
            foreach ( $methods as $key => $actioninfo ) {
                add_filter( $key, $class . '::' . $actioninfo['method'], $actioninfo['priority'], $actioninfo['args'] );
            }
        }
    }

    public static function GetHtmlTable () {
        $html[] = '<table width="100%" border="0" cellspacing="0" cellpadding="5">';
        $html[] = '<tr><th scope="col">Filter</th><th scope="col">Method Name</th></tr>';
        foreach ( self::$_filters as $key => $value ) {
            $html[] = '<tr><td><a href="https://developer.wordpress.org/reference/hooks/' . $key . '" target="_blank" style="text-decoration: none;">' . $key . '</a></td><td>' . $value . '</td></tr>';
        }
        $html[] = '</table>';
        return implode( PHP_EOL, $html );
    }

    public static function GetCount () {
        return count( self::$_filters );
    }

